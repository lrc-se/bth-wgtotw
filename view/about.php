<h1>Om webb&shy;platsen</h1>
<p>
    Här på WGTOTW:s tjänst <strong>Allt om sci-fi</strong> kan du få reda på, ja just det, allt om sci-fi, denna häftigaste av genrer! 
    Skapa ett konto, ställ din fråga och delta i allehanda diskussioner om allehanda företeelser inom SF-världen. 
    Bygg upp ditt rykte genom att skriva många och bra frågor, svar och kommentarer, så att andra medlemmar röstar upp dina inlägg – 
    och utnyttja möjligheten att påverka rangordningen genom att rösta själv. Alla texter skrivs med Markdown, så du har stor frihet att formatera dina inlägg.
</p>
<p>
    Så, vad väntar du på? <a href="<?= $this->url('user/create') ?>">Gå med</a> idag! Vi bits inte, tummis.
</p>
