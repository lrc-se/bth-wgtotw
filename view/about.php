<h1>Om webb&shy;platsen</h1>
<p>
    Denna webbplats är en del i kursen <strong>Webbaserade ramverk 1</strong> i distansprogrammet <em>Webbprogrammering</em> på <a href="http://www.bth.se/">Blekinge Tekniska Högskola</a>. 
    Kursen handlar om "kodstrukturer med ramverk, designmönster och återanvändbara moduler samt tekniker för automatiserad testning och <em>continuous integration</em> i en fullstack-webbmiljö".
</p>
